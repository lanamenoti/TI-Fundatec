
public class Pessoa {
	private String nome;
	private String cpf;
	private Pessoa pai;
	private Pessoa mãe;
	
	public String getNome() {
		return nome;
	}
	
	public void setNome(String nome) {
		this.nome = nome;
	}
	
	public String getCpf() {
		return cpf;
	}
	
	public void setCpf(String cpf) {
		this.cpf = cpf;
	}
	
	public Pessoa getPai() {
		return pai;
	}
	
	public void setPai(Pessoa pai) {
		this.pai = pai;
	}
	
	public Pessoa getMãe() {
		return mãe;
	}
	
	public void setMãe(Pessoa mãe) {
		this.mãe = mãe;
	}
	
	
}
