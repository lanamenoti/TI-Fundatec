
public class Livro {

	private String nome;
	private String autor;
	private String isbn;
	
	public Livro(String nome, String autor, String isbn) {
		this.nome = nome;
		this.autor = autor;
		this.isbn = isbn;
	}

	public String getNome() {
		return nome;
	}

	public String getAutor() {
		return autor;
	}

	public String getIsbn() {
		return isbn;
	}
	
	
	
}
